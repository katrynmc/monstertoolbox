//Eyes
$( "#eyecategory" ).click(function() {
  if ( $( "#eyes:first" ).is( ":hidden" ) ) {
    $( "#eyes" ).show( "slow" );
    $( this ).css( "background-color", "rgba(52, 139, 122, 0.69)" );
  } else {
    $( "#eyes" ).slideUp();
    $( this ).css( "background-color", "black" );
  }
});


